from itertools import product

class CrouchingAmoebas:
    def __init__(self):
        pass

    @classmethod
    def getcost(cls, ax, ay, tx, ty, A):
        cost = 0
        if not (tx <= ax <= tx + A):
            cost += min(abs(ax - tx), abs(ax - (tx+A)))
        if not (ty <= ay <= ty + A):
            cost += min(abs(ay - ty), abs(ay - (ty+A)))
        return cost

    @classmethod
    def count(cls, x, y, A, T):
        res = 0
        for tx,ty in product(x,y):
            li = [cls.getcost(ax,ay,tx,ty,A) for ax,ay in zip(x,y)]
            cnt,t = 0,0
            li.sort()
            for c in li:
                if t + c > T :
                    break
                t += c
                cnt += 1
            res = max(res, cnt)
        return res

